<p class="t1">Términos y condiciones
<p>Para enviar scripts debes de aceptar los siguientes términos y condiciones.
<p><b>1.</b> Al enviar un script, se asegura que posees al menos un conocimiento básico de lo que envías,
ya que de esta forma, cualquier queja, duda o sugerencia deberá ser respondida principalmente por el
autor del script.
<p><b>2.</b> Si el script es copiado de alguna otra web, se deberá respetar la primera regla y se escribirá
el crédito correspondiente al principio del script con el siguiente formato:
<p>Fuente: <b>http://www.pagina.com</b><br>Autor: <b>Nombre</b>
<p><b>3.</b> El script deberá ser explicado claramente, y mencionar cuál es el funcionamiento del código.
<p><b>4.</b> Para los scripts que utilizan base de datos hemos decidido usar un sólo archivo llamado
<b>config.php</b> que se encargará de la conexión a la base de datos, para esto cada vez que requieras una
conexión deberás poner el siguiente código:
<p><b>include("config.php")</b>
<p>Para cerrar la conexión a MySQL simplemente debes usar el siguiente código:
<p><b>mysql_close($conectar)</b>
<p><b>5.</b> Si el webmaster encuentra algún contenido inapropiado lo modificará o eliminará sin previo consentimiento
del autor.
